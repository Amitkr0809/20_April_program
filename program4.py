# Remove List Items

list1 = ["apple", "banana", "cherry"]
list1.remove("banana")
print(list1)

list2 = ["apple", "banana", "cherry"]
list2.pop(1)
print(list2)

list3 = ["apple", "banana", "cherry"]
list3.pop()
print(list3)

list = ["apple", "banana", "cherry"]
del list[0]
print(list)

list4 = ["apple", "banana", "cherry"]
del list4

list5 = ["apple", "banana", "cherry"]
list5.clear()
print(list5)
